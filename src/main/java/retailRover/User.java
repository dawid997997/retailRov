package retailRover;

public class User {
	private String name;
	private String password;
	
	public void setName(String name) {
		this.name=name;
		}
	
	
	public void setPassword(String password) {
		this.password=password;
		}
	
	public String getName() {
		return this.name;
		}
	
	public String getPass() {
		return this.password;
		}
	
	}

